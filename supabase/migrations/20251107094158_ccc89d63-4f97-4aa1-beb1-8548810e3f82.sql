-- Fix search_path for existing functions
CREATE OR REPLACE FUNCTION public.update_classification_patterns_updated_at()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$function$;

